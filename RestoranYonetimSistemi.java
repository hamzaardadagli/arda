     private void yiyecek_hesap(javax.swing.JCheckBox cb,javax.swing.JTextField txt,int fiyat_index){
            if(cb.isSelected() && txt.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Lütfem bir değer giriniz!");
                cb.setSelected(false);
                return;
            }
            int yiyecek_adet;
            try{
                   yiyecek_adet=Integer.parseInt(txt.getText());
                   if(yiyecek_adet<0){
                       JOptionPane.showMessageDialog(null,"Lütfen 0'dan Büyük Bir Sayı Giriniz");
                       cb.setSelected(false);//seçilen yanlış sayıyı silip tekrar değer girmek için hazır hale getirir
                       txt.setText("");
                       return;
                   }
                   
               }
               catch(Exception ex){
                   JOptionPane.showMessageDialog(null,"Lütfen sayısal değer biriniz");
                   return;
               }
              ;
    if(cb.isSelected()){
        yiyecek_total+=yiyecek_adet*yiyecek_fiyat[fiyat_index];
    }else{
          yiyecek_total-=yiyecek_adet*yiyecek_fiyat[0];// TODO add your handling code here:
    }                                     
          lbl_yiyecek.setText(String.format("%.2f TL",yiyecek_total));
     }
