public double hesaplaIfade(String ifade) {
    ifade = ifade.replaceAll("\\s+", ""); // boşlukları sil

    double sayi1 = 0, sayi2 = 0;
    char islem = ' ';
    int islemIndex = -1;

    // işlemi bul
    for (int i = 0; i < ifade.length(); i++) {
        char c = ifade.charAt(i);
        if (c == '+' || c == '-' || c == '*' || c == '/'||c=='%') {
            islem = c;
            islemIndex = i;
            break;
        }
    }

    if (islemIndex == -1) {
        // işlem yok, doğrudan sayı girilmiş
        return Double.parseDouble(ifade);
    }

    try {
        sayi1 = Double.parseDouble(ifade.substring(0, islemIndex));
        sayi2 = Double.parseDouble(ifade.substring(islemIndex + 1));
    } catch (Exception e) {
        return 0;
    }

    switch (islem) {
        case'%':return sayi1%sayi2;
        case '+': return sayi1 + sayi2;
        case '-': return sayi1 - sayi2;
        case '*': return sayi1 * sayi2;
        case '/': return sayi2 != 0 ? sayi1 / sayi2 : 0; // 0'a bölme hatası kontrolü
        default: return 0;
    }
}
    
